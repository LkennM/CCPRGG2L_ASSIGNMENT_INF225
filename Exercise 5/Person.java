public class Person {
    
    private String name;
    private int ccnumber;

    // get() method
    public String getName() {

        return this.name;
    }

    // set() method
    public void setName(String newName) {

        this.name = newName;
    }
    public int getCCnumber() {

        return this.ccnumber;
    }

    // set() method
    public void setCCnumber(int cardnumber) {

        this.ccnumber = cardnumber;

    }

}
